#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
ll modulo = 1000000007;


vector<vector<int>> floyd_warshall(vector<vector<int>> paths)
{
    int size = paths.size();
    int max = 1000;
    vector<vector<int>> distance_matrix(size, vector<int>(size, max));

    for (int i = 0; i < size; i++)
    {
        for (int j = 0; j < size; j++)
        {
            if (paths[i][j] != 0)
            {
                distance_matrix[i][j] = paths[i][j];
            }
        }
    }

    for (int i = 0; i < size; i++)
    {
        distance_matrix[i][i] = 0;
    }

    for (int i = 0; i < size; i++)
    {
        for (int j = 0; j < size; j++)
        {
            for (int k = 0; k < size; k++)
            {
                if (distance_matrix[j][k] > distance_matrix[j][i] + distance_matrix[i][k])
                    distance_matrix[j][k] = distance_matrix[j][i] + distance_matrix[i][k];
            }
        }
    }
    for (int i = 0; i < size; i++)
    {
        for (int j = 0; j < size; j++)
        {
            if (distance_matrix[i][j] == max)
            {
                distance_matrix[i][j] = -1;
            }
        }
    }
    return distance_matrix;
}


vector<vector<ll>> matrix_product(vector<vector<ll>> matrix1, vector<vector<ll>> matrix2)
{
    ll common = matrix1[0].size();
    ll row = matrix1.size();
    ll column = matrix2.size();
    vector<vector<ll>> p(row, vector<ll>(column, 0));
    for (int i = 0; i < row; i++)
    {
        for (int j = 0; j < column; j++)
        {
            for (int k = 0; k < common; k++)
            {
                p[i][j] += matrix1[i][k] * matrix2[k][j];
                p[i][j] %= modulo;
            }
        }
    }
    return p;
}

vector<vector<ll>> matrix_square(vector<vector<ll>> matrix)
{
    return matrix_product(matrix, matrix);
}

vector<vector<ll>> matrix_power(vector<vector<ll>> matrix, ll n)
{
    if (n == 1)
    {
        return matrix;
    }
    if (n == 2)
    {
        return matrix_square(matrix);
    }
    else if (n % 2 == 0)
    {
        return matrix_square(matrix_power(matrix, n / 2));
    }
    else
    {
        return matrix_product(matrix_square(matrix_power(matrix, n / 2)), matrix);
    }
}

void matrix_print(vector<vector<int>> matrix)
{
    for (int i = 0; i < matrix.size(); i++)
    {
        for (int j = 0; j < matrix[0].size(); j++)
        {
            cout << matrix[i][j] << " ";
        }
        cout << "\n";
    }
}

